        'user_id'        => $user->id,
        'coins'          => 0,
        'total_coins'    => 0,
        'total_clicks'   => 0,
        'best_cps'       => 0,
        'owned_upgrades' => [],
    ]);

    Auth::login($user);
    return redirect()->route('game');
    
